let idioma = 'Español';  

switch (idioma) {
    case 'Español':  
        console.log("¡Hola! ¿Cómo estás?");
        break;
    case 'Inglés':  
        console.log("Hello! How are you?");
        break;
    case 'Frances':  
        console.log("Bonjour! Comment ça va?");
        break;
    case 'Alemán':  
        console.log("Hallo! Wie geht's?");
        break;
    case 'Italiano':  
        console.log("Ciao! Come stai?");
        break;
    default:  
        console.log("Idioma desconocido. ¡Saludo!");
}
